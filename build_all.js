// Generate chapter HTML
let allHTML = '';
const sortedChapters = Object.keys(chapters).map(Number).sort((a,b)=>a-b);

for (const num of sortedChapters) {
  let text = chapters[num];
  text = text.replace(/\?\?\?.*?小姐姐我真服了\?\s*/g, ' ');
  text = text.replace(/C\s+hapter/g, 'Chapter');

  const titleMatch = text.match(/^([^.!?\n]+[.!?])/);
  const title = titleMatch ? titleMatch[1].trim() : `Chapter ${num}`;
  let body = titleMatch ? text.substring(titleMatch[0].length) : text;

  allHTML += `\n<h2>Chapter ${num}: ${title}</h2>\n`;

  const sentences = body.split(/(?<=[.!?])\s+/);
  let para = '', sentCount = 0, chBold = 0;

  for (let s of sentences) {
    s = s.trim();
    if (s.length < 3) continue;

    const tokens = s.split(/\b/);
    const newTokens = [];
    for (const token of tokens) {
      const lower = token.toLowerCase();
      if (/^[a-zA-Z]{3,}$/.test(token) && masterSet.has(lower)) {
        newTokens.push(`<b>${token}</b>`);
        chBold++;
      } else {
        newTokens.push(token);
      }
    }
    s = newTokens.join('').replace(/\s{2,}/g, ' ');

    para += s + ' ';
    sentCount++;
    if (sentCount >= 4 || para.length > 500) {
      allHTML += `<p>${para.trim()}</p>\n`;
      para = ''; sentCount = 0;
    }
  }
  if (para.trim()) allHTML += `<p>${para.trim()}</p>\n`;

  console.log(`Chapter ${num}: ${chBold} bold words`);
}

console.log(`\nTotal <b>: ${(allHTML.match(/<b>/g)||[]).length}`);
