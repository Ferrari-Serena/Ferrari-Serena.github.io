// Read novel text
const novel = fs.readFileSync('full_novel_text.txt', 'utf-8');

// Parse chapter vocab
const daily = fs.readFileSync('每日词汇对照表.txt', 'utf-8');

// Extract chapter words
const chapterWords = {};
let currentCh = 0;
for (const line of daily.split('\n')) {
  const m = line.match(/Chapter\s+(\d+)\s/);
  if (m) {
    currentCh = parseInt(m[1]);
    chapterWords[currentCh] = [];
  } else if (currentCh > 0) {
    const words = line.match(/\b([a-zA-Z]{3,})\b/g);
    if (words) chapterWords[currentCh].push(...words.map(w => w.toLowerCase()));
  }
}

// Build master word set
const stopWords = new Set(['the','and','for','are','was','not','its','but','our','has','had','can','may','all','one','two','use','new','old','etc','day','via','fit','sit','bit','yet','ago','nor','off','out','see','way','his','her','who','how','why','what','when','where','that','this','than','then','them','they','with','from','have','been','were','also','some','each','will','more','over','into','just','like','make','made','much','must','only','said','such','take','very','well','your','being','before','after','other','which','their','there','about','would','could','should','people','because','between','chapter','chapters']);
const masterSet = new Set();
for (const words of Object.values(chapterWords)) {
  for (const w of words) {
    if (!stopWords.has(w) && w.length >= 3) masterSet.add(w);
  }
}

// Add derived forms
const extra = [];
for (const w of masterSet) {
  if (w.length >= 4) {
    const suffixes = ['s','ed','ing','ly','ness','ment','tion','able','ible','ance','ence','ity','ive','ize','al'];
    for (const s of suffixes) {
      if (w.endsWith('e') && (s === 'ing' || s === 'ed')) extra.push(w.slice(0,-1) + s);
      extra.push(w + s);
    }
  }
}
for (const e of extra) masterSet.add(e);

console.log(`Master SAT words: ${masterSet.size}`);

// Split novel into chapters
const chPattern = /C\s*hapter\s*(\d+)\s*:/g;
const chMatches = [...novel.matchAll(chPattern)];
const chapters = {};
for (let i = 0; i < chMatches.length; i++) {
  const num = parseInt(chMatches[i][1]);
  const start = chMatches[i].index + chMatches[i][0].length;
  const end = i + 1 < chMatches.length ? chMatches[i+1].index : novel.length;
  chapters[num] = novel.substring(start, end);
}

// Build dictionary
const dict = {};
for (const w of masterSet) {
  if (w.length >= 3) dict[w] = {p:'',c:'',e:''};
}
fs.writeFileSync('merged_dict.json', JSON.stringify(dict), 'utf-8');
console.log(`Dictionary: ${Object.keys(dict).length} entries`);

